public class Person {


    private String nombre;
    private String edad;
    private String sexo;

    public Person() {

    }

    public String retrieveNamePerson() {
        return nombre;
    }

    public void setNamePerson(String nombre) {
        this.nombre = nombre;
    }

    public String getAgePerson() {
        return edad;
    }

    public void setAgePerson(String edad) {
        this.edad = edad;
    }

    public String retrieveSexerson() {
        return sexo;
    }

    public void setSexPerson(String sexo) {
        this.sexo = sexo;
    }

}
